public class jmath {
  public static void main(String[] args) {
    // The Java Math class has many methods that allows you to perform mathematical tasks on numbers.
    System.out.println(Math.max(5, 10)); // find the highest value of x and y
    System.out.println(Math.min(5, 10)); // find the lowest value of x and y
    System.out.println(Math.sqrt(64)); // returns the square root of x
  }
}